"""Pairwise VLM judge (Gemini): of two rollouts under the same control, which
better separates the third-person character (foreground) from the background;
the score is the candidate's win-rate over a reference.
"""
from __future__ import annotations

import hashlib
import json
import os
import time
from pathlib import Path
from typing import Any

from fastvideo.eval.metrics.base import BaseMetric
from fastvideo.eval.models import get_cache_dir
from fastvideo.eval.registry import register
from fastvideo.eval.types import MetricResult, Video

# Part of the on-disk cache key; bump to invalidate cached verdicts.
RUBRIC_ID = "v1"
DEFAULT_MODEL = "gemini-2.5-pro"
DEFAULT_K = 3

SYSTEM_PROMPT = ("You are a strict comparative evaluator of third-person video-game "
                 "rollouts. Two videos were generated by two different models from the "
                 "SAME first frame and the SAME control signal. You judge which video "
                 "better demonstrates that the model SEPARATES the third-person CHARACTER "
                 "(foreground) from the BACKGROUND SCENE — i.e. the control animates the "
                 "character as an INDEPENDENT AGENT with its own trajectory while the "
                 "background moves with the camera. You do NOT reward whichever video "
                 "merely looks cleaner, sharper, or higher-res.")

RUBRIC = """\
You are watching TWO generated video rollouts (Video 1, Video 2) played in full,
from the same first frame under the same control signal. Pick the better
third-person world-model rollout. Judge THREE things together, in this order:

(C) MOTION / ACTION EXECUTION FIRST. The rollout must actually CARRY OUT the
    control signal with substantial motion (the scene/character clearly moves as
    commanded). A clip that is near-static, barely drifts, or only twitches has
    NOT demonstrated controllable separation — it FAILS, no matter how clean it
    looks. CRITICAL: do NOT reward a clip for looking "smoother" or "more
    stable" when that smoothness is really just the ABSENCE OF MOTION. Less
    motion is NOT better. If one clip executes the action with clear motion and
    the other is comparatively static, the MOVING one wins (unless it fails B).

(A) TEMPORAL COHERENCE — among clips that actually move, penalize GENUINE
    corruption: flicker/strobing, texture boiling/crawling, geometry swimming,
    the character or scene morphing/warping into mush, colors pulsing, or
    progressive degradation into noise. Do NOT confuse LEGITIMATE large motion
    (camera sweeping, character running, scene flowing past) with instability —
    fast correct motion is GOOD, not a defect. Only true frame-to-frame
    INCOHERENCE counts against a clip.

(B) FOREGROUND/BACKGROUND SEPARATION — the character stays a distinct, coherent
    entity with its own trajectory while the background responds to the control;
    it does not dissolve/smear into the bg, and the whole frame does not slide
    as one rigid sheet.

Decision: among clips that genuinely execute the motion (C), pick the one that
is both temporally coherent (A) and shows cleaner separation (B). A static or
barely-moving clip loses to a moving one. Real motion is not instability. Do not
reward resolution or placidity. "tie" only if truly equivalent on all three."""

USER_TASK = ("Output a JSON object with four fields:\n"
             " - video_1_analysis: FIRST, how much does Video 1 actually move — does it "
             "execute the control with clear motion, or is it near-static / barely "
             "drifting? THEN: among its motion, is there GENUINE corruption (flicker, "
             "boiling, morphing into mush) as opposed to legitimate fast motion? THEN: "
             "is the character a distinct coherent entity vs rigid-slide / dissolve?\n"
             " - video_2_analysis: the same three checks for Video 2.\n"
             " - comparison: apply (C) motion-first, then (A) genuine-coherence, then "
             "(B) separation. A near-static clip loses to a moving one; legitimate large "
             "motion is NOT a defect.\n"
             " - winner: \"video_1\", \"video_2\", or \"tie\".")

RESPONSE_SCHEMA = {
    "type": "object",
    "properties": {
        "video_1_analysis": {
            "type": "string"
        },
        "video_2_analysis": {
            "type": "string"
        },
        "comparison": {
            "type": "string"
        },
        "winner": {
            "type": "string",
            "enum": ["video_1", "video_2", "tie"]
        },
    },
    "required": ["video_1_analysis", "video_2_analysis", "comparison", "winner"],
    "propertyOrdering": ["video_1_analysis", "video_2_analysis", "comparison", "winner"],
}


def _path_of(sample: dict, key: str) -> str | None:
    """Resolve a native-file path from a string key or a Video wrapper."""
    p = sample.get(f"{key}_path")
    if isinstance(p, str):
        return p
    v = sample.get(key)
    if isinstance(v, Video) and isinstance(v.source, str):
        return v.source
    if isinstance(v, str):
        return v
    return None


def _resolve_api_key() -> str:
    for env in ("GEMINI_API_KEY", "GOOGLE_API_KEY"):
        key = os.environ.get(env)
        if key:
            return key.strip()
    token = Path("~/.gemini_token").expanduser()
    if token.is_file():
        return token.read_text().strip()
    raise ValueError("judge.third_person_separation needs a Gemini API key. Set "
                     "GEMINI_API_KEY (or GOOGLE_API_KEY), or write it to ~/.gemini_token.")


@register("judge.third_person_separation")
class ThirdPersonSeparationMetric(BaseMetric):
    """Pairwise VLM judge of third-person fg/bg separation; corpus win-rate."""

    name = "judge.third_person_separation"
    requires_reference = True
    higher_is_better = True
    needs_gpu = False
    is_set_metric = True
    dependencies = ["google.genai"]

    def __init__(self, model: str = DEFAULT_MODEL, k: int = DEFAULT_K) -> None:
        super().__init__()
        self.model = model
        self.k = k
        self._client: Any = None
        self._files: dict[str, Any] = {}  # path -> uploaded Gemini file handle
        self._records: list[dict] = []  # one per accumulated pair
        self._pairs_seen = 0  # drives A/B counterbalancing

    # --- model / client -----------------------------------------------------
    def setup(self) -> None:
        if self._client is not None:
            return
        from google import genai
        self._client = genai.Client(api_key=_resolve_api_key())

    # --- set-vs-set protocol ------------------------------------------------
    def reset(self) -> None:
        self._records = []
        self._pairs_seen = 0
        self._files = {}

    def accumulate(self, sample: dict) -> None:
        cand = _path_of(sample, "video")
        base = _path_of(sample, "reference")
        if cand is None or base is None:
            return  # nothing to compare

        image = sample.get("image_path")
        action_text = sample.get("text_prompt") or ""
        action = sample.get("action")

        rec = self._cached(cand, base, action_text)
        if rec is None:
            if self._client is None:
                self.setup()
            rec = self._judge_pair(cand, base, image, action_text)
            self._write_cache(cand, base, action_text, rec)
        rec = {**rec, "action": action}
        self._records.append(rec)
        self._pairs_seen += 1

    def finalize(self) -> MetricResult:
        recs = [r for r in self._records if r.get("verdict")]
        if not recs:
            return MetricResult(name=self.name, score=None, details={"skipped": "no pairs judged"})
        wins = sum(r["verdict"] == "candidate" for r in recs)
        losses = sum(r["verdict"] == "baseline" for r in recs)
        ties = sum(r["verdict"] == "tie" for r in recs)
        decided = wins + losses
        score = wins / decided if decided else None

        # Per-action win-rate, grouped by the raw label (no assumed control scheme).
        per_action: dict[str, dict] = {}
        for action in sorted({r.get("action") for r in recs if r.get("action")}):
            gr = [r for r in recs if r.get("action") == action]
            gw = sum(r["verdict"] == "candidate" for r in gr)
            gl = sum(r["verdict"] == "baseline" for r in gr)
            per_action[action] = {
                "n": len(gr),
                "wins": gw,
                "losses": gl,
                "ties": len(gr) - gw - gl,
                "win_rate": (gw / (gw + gl)) if (gw + gl) else None,
            }
        return MetricResult(name=self.name,
                            score=score,
                            details={
                                "wins": wins,
                                "losses": losses,
                                "ties": ties,
                                "n": len(recs),
                                "win_rate_excl_ties": score,
                                "per_action": per_action,
                            })

    def merge_from(self, other: BaseMetric) -> None:
        assert isinstance(other, ThirdPersonSeparationMetric)
        self._records.extend(other._records)
        self._pairs_seen += other._pairs_seen

    # --- judging ------------------------------------------------------------
    def _judge_pair(self, cand: str, base: str, image: str | None, action_text: str) -> dict:
        """k counterbalanced comparisons → aggregated per-pair verdict."""
        mapped: list[str] = []
        for i in range(self.k):
            cand_first = (self._pairs_seen + i) % 2 == 0
            v1, v2 = (cand, base) if cand_first else (base, cand)
            winner = self._one_call(image, v1, v2, action_text)
            if winner == "tie":
                mapped.append("tie")
            elif (winner == "video_1") == cand_first:
                mapped.append("candidate")
            else:
                mapped.append("baseline")
        cand_w = mapped.count("candidate")
        base_w = mapped.count("baseline")
        verdict = ("candidate" if cand_w > base_w else "baseline" if base_w > cand_w else "tie")
        return {
            "verdict": verdict,
            "candidate_wins": cand_w,
            "baseline_wins": base_w,
            "ties": mapped.count("tie"),
            "k": self.k,
            "rubric_id": RUBRIC_ID
        }

    def _one_call(self, image: str | None, vid1: str, vid2: str, action_text: str) -> str:
        from google.genai import types
        contents: list[Any] = [action_text or "Compare these two rollouts."]
        if image is not None:
            contents += ["\nFirst frame (input condition, shared by BOTH "
                         "videos):", self._upload(image)]
        contents += [
            "\nVideo 1 (model A's full rollout — watch it in motion):",
            self._upload(vid1),
            "\nVideo 2 (model B's full rollout — watch it in motion):",
            self._upload(vid2),
            "\n" + RUBRIC + "\n\n" + USER_TASK,
        ]
        for attempt in range(6):
            try:
                resp = self._client.models.generate_content(model=self.model,
                                                            contents=contents,
                                                            config=types.GenerateContentConfig(
                                                                system_instruction=SYSTEM_PROMPT,
                                                                response_mime_type="application/json",
                                                                response_schema=RESPONSE_SCHEMA,
                                                                temperature=0.4))
                return json.loads(resp.text).get("winner", "tie")
            except Exception as exc:  # noqa: BLE001 - transient API errors
                if attempt == 5:
                    raise
                is_429 = "429" in str(exc) or "RESOURCE_EXHAUSTED" in str(exc)
                time.sleep(40 if is_429 else 2**attempt)
        return "tie"

    def _upload(self, path: str) -> Any:
        f = self._files.get(path)
        if f is not None:
            return f
        f = self._client.files.upload(file=path)
        while f.state.name != "ACTIVE":
            time.sleep(1)
            f = self._client.files.get(name=f.name)
            if f.state.name == "FAILED":
                raise RuntimeError(f"Gemini upload failed for {path}")
        self._files[path] = f
        return f

    # --- per-pair cache -----------------------------------------------------
    def _cache_path(self, cand: str, base: str, action_text: str) -> Path:
        h = hashlib.sha1(f"{RUBRIC_ID}|{self.model}|{self.k}|{cand}|{base}|{action_text}".encode()).hexdigest()[:16]
        return get_cache_dir() / "judge" / "third_person_separation" / f"{h}.json"

    def _cached(self, cand: str, base: str, action_text: str) -> dict | None:
        cp = self._cache_path(cand, base, action_text)
        if not cp.is_file():
            return None
        try:
            rec = json.loads(cp.read_text())
        except Exception:
            return None
        return rec if rec.get("k", 0) >= self.k and "verdict" in rec else None

    def _write_cache(self, cand: str, base: str, action_text: str, rec: dict) -> None:
        cp = self._cache_path(cand, base, action_text)
        cp.parent.mkdir(parents=True, exist_ok=True)
        cp.write_text(json.dumps(rec, indent=2))
